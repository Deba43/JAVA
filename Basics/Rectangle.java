package Basics;

public class Rectangle {
    public static void main(String[] args) {
        double l = 45.0;
        double w = 5.0;
        double area = l * w;
        double pm = 2 * (l + w);
        System.out.println("The area of Rectangle with length " + l + " and width " + w + " is " + area);
        System.out.println("The perimeter of rectangle with length " + l + " and width " + w + " is " + pm);

    }
}
